namespace PlexDL.Player
{
    /// <summary>
    /// Specifies the audio format.
    /// </summary>
    public enum AudioFormat
    {
        /// <summary>
        /// Represents Advanced Audio Coding (.m4a) audio.
        /// </summary>
        AAC,
        /// <summary>
        /// Represents MPEG Audio Layer-3 (.mp3) audio.
        /// </summary>
        MP3,
        /// <summary>
        /// Represents Windows Media Audio 9 Professional or Windows Media Audio 9.1 Professional (.wma) audio.
        /// </summary>
        WMA9
    }
}