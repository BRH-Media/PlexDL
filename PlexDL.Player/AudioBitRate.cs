namespace PlexDL.Player
{
    /// <summary>
    /// Specifies the audio bitrate.
    /// </summary>
    public enum AudioBitRate
    {
        /// <summary>
        /// Represents 32 Kbit (4000 bytes) per second.
        /// </summary>
        Kbps_032 = 4000,
        /// <summary>
        /// Represents 64 Kbit (8000 bytes) per second.
        /// </summary>
        Kbps_064 = 8000,
        /// <summary>
        /// Represents 96 Kbit (12000 bytes) per second.
        /// </summary>
        Kbps_096 = 12000,
        /// <summary>
        /// Represents 128 Kbit (16000 bytes) per second.
        /// </summary>
        Kbps_128 = 16000,
        /// <summary>
        /// Represents 160 Kbit (20000 bytes) per second.
        /// </summary>
        Kbps_160 = 20000,
        /// <summary>
        /// Represents 192 Kbit (24000 bytes) per second.
        /// </summary>
        Kbps_192 = 24000,
        /// <summary>
        /// Represents 256 Kbit (32000 bytes) per second.
        /// </summary>
        Kbps_256 = 32000,
        /// <summary>
        /// Represents 320 Kbit (40000 bytes) per second.
        /// </summary>
        Kbps_320 = 40000
    }
}