namespace PlexDL.Player
{
    /// <summary>
    /// Specifies the video format.
    /// </summary>
    public enum VideoFormat
    {
        /// <summary>
        /// Represents H.264/AVC (.mp4) video.
        /// </summary>
        H264,
        /// <summary>
        /// Represents H.265/HEVC (.mp4) video.
        /// </summary>
        H265,
        /// <summary>
        /// Represents Windows Media Video 9 (.wmv) video.
        /// </summary>
        WMV9
    }
}