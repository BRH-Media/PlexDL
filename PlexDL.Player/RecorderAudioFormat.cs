namespace PlexDL.Player
{
    /// <summary>
    /// Specifies the audio format recorded with the recorder.
    /// </summary>
    public enum RecorderAudioFormat
    {
        /// <summary>
        /// Advanced Audio Coding (AAC).
        /// </summary>
        AAC,
        /// <summary>
        /// MPEG Audio Layer-3 (MP3).
        /// </summary>
        MP3,
        /// <summary>
        /// Uncompressed PCM audio.
        /// </summary>
        PCM
    }
}